	<li>Workplace conflict</li>
	<li>Major life transitions</li>
	<li>Health related issues for self or partner</li>
	<li>Parenting concerns</li>
	<li>Extended family issues</li>
	<li>Care for an ailing partner or family member</li>
</ul>

<p>Consultation and coaching services are typically private pay.</p>

<p><b>Psychotherapy:</b> Psychotherapy typically, but not always, involves treatment for a mental health condition (e.g. Anxiety, Depression). In this case the person seeking services, maybe experiencing symptoms that are distressing and interfering with their daily functioning.</p>

<p>Initially we work to reduce the symptoms (e.g. anxiety, depression) and then to understand and address the underlying patterns that have contributed to the development of the problem. Our goal is an increased sense of mastery and control over how you live your life.</p>

<p>Treatment specialization areas include:</p>
<ul>
	<li>Anxiety</li>
	<li>Depression</li>
	<li>Trauma history</li>
	<li>Abuse history</li>
	<li>Grief issues</li>
	<li>ADHD</li>
	<li>Anger issues</li>
</ul>

<p>These services may involve a medical diagnosis and therefore can be billed through your medical insurance.</p>
