<h1>Services</h1>

<p>I work with individuals and couples, providing psychotherapy, counseling and coaching.</p>

<p>I have been in private practice in the Hillsboro/Beaverton area for 15 years.  In that time I have worked with a broad diversity of men and women.  For many, this is their first time seeking therapy.  The decision to seek out help is difficult one for most people.  I appreciate the challenges and awkwardness inherent in this work and try to insure the experience is comfortable and helpful.</p>

<p>There are a myriad of situations that might lead someone to therapy:</p>
<ul>
	<li>Unexpected life events</li>
	<li>Changes in core relationships</li>
	<li>Marital frustration and disappointment</li>
	<li>Career stagnation</li>
	<li>New child</li>
	<li>Care for ailing partner or family member</li>
	<li>Health related issues</li>
</ul>

